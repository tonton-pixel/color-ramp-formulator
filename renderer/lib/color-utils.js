function hexToRgb (hexColorString)
{
    let color;
    let result = hexColorString.match (/^#?([A-Fa-f0-9]{6}|[A-Fa-f0-9]{3})$/);
    if (result)
    {
        let rgb = result[1];
        if (rgb.length === 3)
        {
            rgb = rgb[0] + rgb[0] + rgb[1] + rgb[1] + rgb[2] + rgb[2];
        }
        color =
        [
            parseInt (rgb.slice (0, 2), 16),
            parseInt (rgb.slice (2, 4), 16),
            parseInt (rgb.slice (4, 6), 16)
        ];
    }
    else
    {
        throw new Error ("Invalid HTML/CSS hex color string: " + hexColorString);
    }
    return color;
}
