    let green = rgbColor[1];
    let blue = rgbColor[2];
    red /= 255;
    green /= 255;
    blue /= 255;
    function inverseGamma (n)
    {
        // sRGB inverse gamma correction
        return (n > 0.04045) ? Math.pow ((n + 0.055) / 1.055, 2.4) : n / 12.92;
    }
    red = inverseGamma (red);
    green = inverseGamma (green);
    blue = inverseGamma (blue);
    // Observer angle = 2°, Illuminant = D65
    let x = (red * 0.4124564) + (green * 0.3575761) + (blue * 0.1804375);
    let y = (red * 0.2126729) + (green * 0.7151522) + (blue * 0.0721750);
    let z = (red * 0.0193339) + (green * 0.1191920) + (blue * 0.9503041);
