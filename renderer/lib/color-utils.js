function ycbcrToRgb (ycbcrColor, ycbcrFractional, rgbFractional)
{
    let y = ycbcrColor[0];
    let cb = ycbcrColor[1];
    let cr = ycbcrColor[2];
    if (ycbcrFractional)
    {
        y *= 255;
        cb *= 255;
        cr *= 255;
    }
    let red = y + (1.402 * (cr - 128));
    let green = y - (0.34414 * (cb - 128)) - (0.71414 * (cr - 128));
    let blue = y + (1.772 * (cb - 128));
    if (rgbFractional)
    {
        red /= 255;
        green /= 255;
        blue /= 255;
    }
    return [ red, green, blue ];
}
