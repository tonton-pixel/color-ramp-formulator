function colorToRgb (color)
{
    let rgb = null;
    if (typeof color === 'string')
    {
        color = color.trim ();
        if (color[0] === '#')
        {
            rgb = hexToRgb (color);
        }
        else
        {
            let defaultSetName = "W3C";
            let names = color.split (/[:\/]/);
            let setName;
            let colorName;
            if (names.length > 1)
            {
                setName = names[0] || defaultSetName;
                colorName = names[1];
            }
            else
            {
                setName = defaultSetName;
                colorName = names[0];
            }
            color = nameToColor (setName, colorName, true);
            if (color[0] === "RGBColor")
            {
                rgb = color[1];
            }
            else
            {
                throw new Error ("Unsupported named color set name: " + setName);
            }
        }
    }
    else if (Array.isArray (color) && (color.length === 3))
    {
        rgb = color;
    }
    else
    {
        throw new Error ("Invalid RGB color syntax");
    }
    return rgb;
}
