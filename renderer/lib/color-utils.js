function getNamedColorsSet (setName)
{
    let namedColorsSet;
    setName = setName.replace (/[\s_]/g, "").toLowerCase ();
    if (setName in namedColorsSets)
    {
        namedColorsSet = namedColorsSets[setName];
        if (typeof namedColorsSet === 'string')
        {
            namedColorsSet = namedColorsSets[namedColorsSet];
        }
    }
    else
    {
        throw new Error ("Unknown named colors set: " + setName);
    }
    return namedColorsSet;
}
//
function enumerateNamedColors (setName, excludeVariants)
{
    let namedColorsSet = getNamedColorsSet (setName);
    let colorNames = namedColorsSet["colorNames"];
    let names = [ ];
    for (let colorName in colorNames)
    {
        if (colorNames.hasOwnProperty (colorName))
        {
            if ((!excludeVariants) || (typeof colorNames[colorName] !== 'string'))
            {
                names.push (colorName);
            }
        }
    }
    return names;
}
