function hsvToRgb (hsvColor, hsvFractional, rgbFractional)
{
    let hue = hsvColor[0];
    let saturation = hsvColor[1];
    let value = hsvColor[2];
    if (!hsvFractional)
    {
        hue /= 360;
        saturation /= 100;
        value /= 100;
    }
    hue -= Math.floor (hue);
    saturation = limit (saturation, 0, 1);
    value = limit (value, 0, 1);
    let red;
    let green;
    let blue;
    let i = Math.floor (hue * 6);
    let f = (hue * 6) - i;
    let p = value * (1 - saturation);
    let q = value * (1 - (f * saturation));
    let t = value * (1 - ((1 - f) * saturation));
    switch (i % 6)
    {
        case 0:
            red = value;
            green = t;
            blue = p;
            break;
        case 1:
            red = q;
            green = value;
            blue = p;
            break;
        case 2:
            red = p;
            green = value;
            blue = t;
            break;
        case 3:
            red = p;
            green = q;
            blue = value;
            break;
        case 4:
            red = t;
            green = p;
            blue = value;
            break;
        case 5:
            red = value;
            green = p;
            blue = q;
            break;
    }
    if (!rgbFractional)
    {
        red *= 255;
        green *= 255;
        blue *= 255;
    }
    return [ red, green, blue ];
};
